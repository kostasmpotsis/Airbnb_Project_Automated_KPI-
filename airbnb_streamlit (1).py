def kpi_totals(df):
    return {
        "revenue":      df["PAYMENT"].sum() if "PAYMENT" in df.columns else 0,
        "revenue_net":  df["PAYMENT_NET"].sum() if "PAYMENT_NET" in df.columns else 0,
        "booked_days":  df["TOTAL_BOOKED_DAYS"].sum() if "TOTAL_BOOKED_DAYS" in df.columns else 0,
        "avg_occ":      df["OCCUPANCY_RATE"].mean() if "OCCUPANCY_RATE" in df.columns else 0,
        "avg_adr":      df["AVERAGE_DAILY_RATE"].mean() if "AVERAGE_DAILY_RATE" in df.columns else 0,
        "avg_revpar":   df["REVENUE_PER_AVAILABLE_ROOM"].mean() if "REVENUE_PER_AVAILABLE_ROOM" in df.columns else 0,
    }

