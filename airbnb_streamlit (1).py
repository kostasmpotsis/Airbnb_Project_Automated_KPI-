def sort_kpi(df: pd.DataFrame) -> pd.DataFrame:
    if "MONTH" in df.columns:
        df["_mo"] = pd.Categorical(df["MONTH"], categories=MONTH_ORDER, ordered=True)
        df = df.sort_values("_mo").drop(columns="_mo")
    return df.reset_index(drop=True)

